from django.contrib import admin
from .models import ExamOfficer, Notification, SystemReport

@admin.register(ExamOfficer)
class ExamOfficerAdmin(admin.ModelAdmin):
    list_display = ('get_full_name', 'officer_id', 'email', 'is_active')
    list_filter = ('is_active', 'registration_date')
    search_fields = ('user__first_name', 'user__last_name', 'officer_id', 'email')
    readonly_fields = ('registration_date', 'updated_at')
    
    def get_full_name(self, obj):
        return obj.user.get_full_name()
    get_full_name.short_description = 'Name'

@admin.register(Notification)
class NotificationAdmin(admin.ModelAdmin):
    list_display = ('title', 'notification_type', 'recipient', 'is_read', 'created_at')
    list_filter = ('notification_type', 'is_read', 'created_at')
    search_fields = ('title', 'message', 'recipient__email')
    readonly_fields = ('created_at', 'read_at')
    
    fieldsets = (
        ('Content', {'fields': ('title', 'message', 'notification_type')}),
        ('Recipients', {'fields': ('recipient', 'created_by')}),
        ('Status', {'fields': ('is_read',)}),
        ('Dates', {'fields': ('created_at', 'read_at')}),
    )

@admin.register(SystemReport)
class SystemReportAdmin(admin.ModelAdmin):
    list_display = ('title', 'report_type', 'generated_by', 'generated_date', 'is_archived')
    list_filter = ('report_type', 'generated_date', 'is_archived', 'faculty', 'department')
    search_fields = ('title', 'description', 'generated_by__email')
    readonly_fields = ('generated_date', 'updated_date')
    
    fieldsets = (
        ('Report Information', {'fields': ('title', 'description', 'report_type')}),
        ('Organizational', {'fields': ('faculty', 'department', 'program')}),
        ('Generated By', {'fields': ('generated_by',)}),
        ('Status', {'fields': ('is_archived',)}),
        ('Dates', {'fields': ('generated_date', 'updated_date')}),
    )

